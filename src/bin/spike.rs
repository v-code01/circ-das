//! Task-0 d/n-vs-rate SPIKE driver.
//!
//! Emits `bench_results/spike.md`: the first measured minimum-distance-ratio (d/n)
//! vs code-rate curve for block-circulant local codes (arXiv:2406.12160) vs 2D
//! Reed-Solomon, plus a small fully-measured GF(2^8) cross-check and the
//! encode->erase->recover roundtrip confirmation (the sacred DA invariant).

use circ_das::block_circulant::BlockCirculant;
use circ_das::gf256::Gf256;
use circ_das::grs::min_distance_from_h;
use circ_das::rs2d::Rs2d;
