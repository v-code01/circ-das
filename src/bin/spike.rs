fn yn(b: bool) -> &'static str {
    if b { "YES" } else { "NO" }
}

fn roundtrip_bc(f: &Gf256) -> bool {
    let bc = BlockCirculant::new(f, 6, 2, 2);
    let msg: Vec<u8> = (0..bc.k() as u8).map(|x| x.wrapping_mul(7).wrapping_add(1)).collect();
    let cw = bc.encode(f, &msg);
    let n = bc.n();
    // exhaustive all subsets of size <= 2*rho
    let two_rho = 2 * bc.rho;
    for t in 1..=two_rho {
        let mut idx = vec![0usize; t];
        if !bc_check(f, &bc, &cw, n, &mut idx, 0, 0) {
            return false;
        }
    }
    true
}

fn bc_check(f: &Gf256, bc: &BlockCirculant, cw: &[u8], n: usize, idx: &mut Vec<usize>, start: usize, depth: usize) -> bool {
    if depth == idx.len() {
        let mut dmg = cw.to_vec();
        for &e in idx.iter() { dmg[e] = 0; }
        return match bc.recover(f, &dmg, idx) { Some(r) => r == cw, None => false };
    }
    for j in start..n {
        idx[depth] = j;
        if !bc_check(f, bc, cw, n, idx, j + 1, depth + 1) { return false; }
    }
    true
}

fn roundtrip_rs(f: &Gf256) -> bool {
    let code = Rs2d::new(f, 5, 3, 5, 3);
    let msg: Vec<u8> = (0..code.k() as u8).map(|x| x.wrapping_mul(9).wrapping_add(7)).collect();
    let grid = code.encode(f, &msg);
    let d = code.d_formula();
    let n = code.n();
    let mut state: u64 = 0xdead_beef_1234_5678;
    let mut next = || { state = state.wrapping_mul(6364136223846793005).wrapping_add(1); (state >> 33) as usize };
    for _ in 0..200 {
        let mut chosen = std::collections::BTreeSet::new();
        while chosen.len() < d - 1 { chosen.insert(next() % n); }
        let erased: Vec<usize> = chosen.into_iter().collect();
        let mut dmg = grid.clone();
        for &e in &erased { dmg[e] = 0; }
        match code.recover(f, &dmg, &erased) { Some(r) if r == grid => {}, _ => return false }
    }
    true
}
