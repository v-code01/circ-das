    let _ = writeln!(md, "- Local codes (overlap factor lambda): GRS `[lambda*omega + rho, lambda*omega, rho+1]`.");
    let _ = writeln!(md, "- Global code: `n = mu*(rho+omega)`, `k = mu*omega`, rate `R = omega/(rho+omega)`.");
    let _ = writeln!(md, "- lambda=2 (this spike, mu = 2*nu even): Theorem III.2 => minimum distance `d = 2*rho + 1`;");
    let _ = writeln!(md, "  erasure decoder recovers every pattern of `<= 2*rho` erasures.");
    let _ = writeln!(md, "- 2D Reed-Solomon baseline (Celestia / arXiv:1809.09044): square product code,");
    let _ = writeln!(md, "  `n = n1^2`, `k = k1^2`, `d = (n1-k1+1)^2`; corrects all `< d` erasures.\n");

    let _ = writeln!(md, "## Distance-computation method\n");
    let _ = writeln!(md, "Both codes are linear; erasure-correctability of a set E <=> the columns of the");
    let _ = writeln!(md, "parity check indexed by E are linearly independent, so d = smallest number of");
